package javapackage;

public class PatternSolidRectangle {

	public static void main(String[] args) {
		//Patterns 
		
		int m = 5; //for rows
		int n = 6; //for columns
		
		for (int i=1; i<=m; i++) {
			
			for(int j=1; j<=n; j++) {
				
				System.out.print("*");
				}
			System.out.println();
		}
	}

}

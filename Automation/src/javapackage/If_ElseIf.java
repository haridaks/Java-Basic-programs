package javapackage;

public class If_ElseIf {
//it will execute multiple conditions
	
	public static void main(String[] args) {
		
    int a = 100;
    int b = 200;
    if(a>b) {
    	System.out.println("A is greater");
    }
    else if(a==b) {
    	System.out.println("A is equal to b");
    }
    else {
    	System.out.println("B is greater");
    }
	
	}
}
